import { describe, expect, it } from 'vitest'

import { hkdfExpand } from '../hkdf'

describe('hkdfExpand', () => {
  it('matches RFC 5869 test vector (SHA-256)', async () => {
    const parent = Uint8Array.from([
      0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
      0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
    ])
    const salt = Uint8Array.from([0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c])
    const info = Uint8Array.from([0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7, 0xf8, 0xf9])

    const okm = await hkdfExpand({ parent, salt, info, length: 32 })

    expect(Buffer.from(okm).toString('hex')).toEqual(
      '3cb25f25faacd57a90434f64d0362f2a2d2d0a90cf1a5a4c5db02d56ecc4c5bf',
    )
  })
})
